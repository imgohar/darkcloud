                     <h2>The advantage of BGP</h2>
                     <p>Border Gateway Protocol (BGP) is an autonomous inter-system routing protocol that replaced the External Gateway Protocol (EGP) as a routing exchange protocol. It enables data to be transferred between autonomous systems used for OVHcloud Connect. Compared to internal routing protocols (e.g. RIP or IGRP), it does not take into account the distance between a router and a destination — instead, it takes a more precise set of parameters that can be defined. Its main advantage is its ability to handle a large number of routes that can be selected according to a specific policy. For example, you may choose to limit the number of connected autonomous systems, refuse transit, or prioritise some systems over others. This means it provides protection against loops for autonomous systems. To a lesser extent, it also enables you to manage the traffic load depending on the network announcement.</p>

